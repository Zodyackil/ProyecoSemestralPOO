    public void Buscar() throws SQLException {
        String busqueda = txtBuscar.getText();
        int stock = Integer.parseInt(txtCantidad.getText());
        modelo = new DefaultTableModel(null, titulos);
        switch (busqueda) {

            case "ean":
                try {
                    stmt = cn.createStatement();
                    ResultSet rs = stmt.executeQuery("SELECT * FROM productos WHERE ean = '" + busqueda + "'");

                    while (rs.next()) {
                        String ean = rs.getString("ean");
                        int cantidad = Integer.parseInt(rs.getString("cantidad"));

                        if ((stock) >= (cantidad)) {
                            
                            if (busqueda.equals(ean)) {

                                filas[0] = rs.getString("ean");
                                filas[1] = rs.getString("codigo");
                                filas[2] = rs.getString("nombre");
                                filas[3] = rs.getString("modelo");
                                filas[4] = rs.getString("pre_venta");

                                modelo.addRow(filas);
                            } else {
                                JOptionPane.showMessageDialog(null, "No se encontro el producto");
                            }
                        } else {
                            
                        }
                    }

                    tablaVenta.setModel(modelo);

                } catch (SQLException ex) {
                    Logger.getLogger(MantenedorModificar.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    cn.close();
                }
                break;

            default:
                try {
                    stmt = cn.createStatement();
                    ResultSet rs = stmt.executeQuery("SELECT * FROM productos WHERE codigo = '" + busqueda + "'");

                    while (rs.next()) {
                        String codigo = rs.getString("codigo");

                        if (busqueda.equals(codigo)) {

                            filas[0] = rs.getString("ean");
                            filas[1] = rs.getString("codigo");
                            filas[2] = rs.getString("nombre");
                            filas[3] = rs.getString("modelo");
                            filas[4] = rs.getString("pre_venta");

                            modelo.addRow(filas);
                        } else {
                            JOptionPane.showMessageDialog(null, "No se encontro el producto");
                        }
                    }

                    tablaVenta.setModel(modelo);

                } catch (SQLException ex) {
                    Logger.getLogger(MantenedorModificar.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    cn.close();
                }
                break;

        }
    }

